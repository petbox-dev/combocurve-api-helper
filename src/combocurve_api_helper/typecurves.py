class TypeCurves(APIBase):
    ######
    # URLs
    ######

    def get_type_curves_url(self, project_id: str, filters: Optional[Dict[str, str]] = None) -> str:
        """
        Returns the API url of type curves for a specific project id.
        """
        url = f'{self.API_BASE_URL}/projects/{project_id}/type-curves'
        if filters is None:
            return url

        url += self._build_params_string(filters)
        return url

    def get_type_curve_by_id_url(self, project_id: str, type_curve_id: str) -> str:
        """
        Returns the API url for a specific type curve from its type curve id.
        """
        base_url = self.get_type_curves_url(project_id)
        return f'{base_url}/{type_curve_id}'

    def get_type_curve_representative_wells_url(self, project_id: str, type_curve_id: str) -> str:
        """
        Returns the API url for representative wells for a specific project id
        and type curve id.
        """
        base_url = self.get_type_curve_by_id_url(project_id, type_curve_id)
        return f'{base_url}/representative-wells'

    def get_type_curve_daily_fits_url(self, project_id: str, type_curve_id: str) -> str:
        """
        Returns the API url for daily fits for a specific project id and
        type curve id.
        """
        base_url = self.get_type_curve_by_id_url(project_id, type_curve_id)
        return f'{base_url}/daily-fits'

    def get_type_curve_monthly_fits_url(self, project_id: str, type_curve_id: str) -> str:
        """
        Returns the API url for monthly fits for a specific project id and
        type curve id.
        """
        base_url = self.get_type_curve_by_id_url(project_id, type_curve_id)
        return f'{base_url}/monthly-fits'

    ###########
    # API calls
    ###########

    def post_type_curves(self, project_id: str, data: ItemList) -> ItemList:
        """
        Creates one or more type curves for a specific project id.

        https://docs.api.combocurve.com/api/post-type-curves
        """
        url = self.get_type_curves_url(project_id)
        return self._post_items(url, data)

    def put_type_curves(self, project_id: str, data: ItemList) -> ItemList:
        """
        Upserts one or more type curves for a specific project id.

        https://docs.api.combocurve.com/api/put-type-curves
        """
        url = self.get_type_curves_url(project_id)
        return self._put_items(url, data)

    def delete_type_curves(
        self, project_id: str, name: Optional[str] = None, id: Optional[str] = None
    ) -> CaseInsensitiveDict[str]:
        """
        Deletes type curves for a specific project id, filtered by `name` or `id`
        (at least one is required; the API rejects an unfiltered delete). Returns
        the delete response headers, where 'X-Delete-Count' is the number deleted.

        https://docs.api.combocurve.com/api/delete-type-curves
        """
        if (name or id) is None:
            raise ValueError('Must provide at least one of name or id')

        filters: Dict[str, str] = {}
        if name is not None:
            filters['name'] = name
        if id is not None:
            filters['id'] = id

        url = self.get_type_curves_url(project_id, filters)
        responses = self._delete_responses(url, data=[])

        return responses[0].headers

